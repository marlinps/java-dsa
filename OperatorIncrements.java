import java.util.*;

public class OperatorIncrements {
    public static void main(String[] args) {
        //TODO: Pre Increment ++a
        int a = 10;
        int b = 0;

        b = ++a;
        System.out.println(a);
        System.out.println(b);

        //TODO: Post Increment a++
        int a1 = 10;
        int b1 = 0;

        b1 = a1++;
        System.out.println(a1);
        System.out.println(b1);
    }
}
